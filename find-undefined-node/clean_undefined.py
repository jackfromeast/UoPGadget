import os;
import sys;
import json;

undefined = []
undefined_no_repeat = []

def get_undefined(line):
  if "KeyName::" in line:
    index = line.find("#")
    if index != -1:
      name = line[index + 1:]
      undefined.append(name.strip())

def main():
  args = sys.argv[1:]
  path = args[0]

  f = open(path, 'r')

  need = False
  for line in f:
    if line == "===========start===========\n":
      need = True
      continue
    elif line == "===========end===========\n":
      need = False
      continue
    elif need:
      get_undefined(line)

  undefined_no_repeat = list(set(undefined))
  # generate json format array for undefined properties
  undefined_no_repeat_json = json.dumps(undefined_no_repeat)
  print(undefined_no_repeat_json)

if __name__ == '__main__':
  main()


